async function printAndShare(html: string, filename: string): Promise<void> {
  try {
    const canShare = await Sharing.isAvailableAsync();
    if (canShare) {
      const { uri } = await Print.printToFileAsync({ html, base64: false });
      await Sharing.shareAsync(uri, {
        mimeType: 'application/pdf',
        dialogTitle: filename,
        UTI: 'com.adobe.pdf',
      });
    } else {
      // Fall back to the print dialog on platforms where sharing is unavailable
      await Print.printAsync({ html });
    }
  } catch (e: any) {
    Alert.alert('Export failed', e?.message ?? String(e));
  }
}

// ─── Trial Balance PDF ──────────────────────────────────────────────────────

export async function exportTrialBalancePDF(params: {
  rows: TrialBalanceRow[];
  companyName: string;
  asOf: string;
  totalDebit: number;
  totalCredit: number;
}): Promise<void> {
  const { rows, companyName, asOf, totalDebit, totalCredit } = params;
  const isBalanced = Math.abs(totalDebit - totalCredit) < 0.01;

  const tableRows = rows.map((r) => {
    const indent = (r.level ?? 0) * 16;
    const name = r.account_code ? `${r.account_code} — ${r.account_name ?? ''}` : (r.account_name ?? '');
    const dr = r.debit > 0 ? formatCurrency(r.debit) : '';
    const cr = r.credit > 0 ? formatCurrency(r.credit) : '';
    return `<tr${r.is_group ? ' class="group"' : ''}>
      <td style="padding-left:${10 + indent}px">${name}</td>
      <td class="right${!r.debit ? ' muted' : ''}">${dr || '—'}</td>
      <td class="right${!r.credit ? ' muted' : ''}">${cr || '—'}</td>
    </tr>`;
  }).join('');

  const body = `
    <div class="report-header">
      <div class="report-title">Trial Balance</div>
      <div class="report-meta">Company: ${companyName} &nbsp;·&nbsp; As of: ${asOf}</div>
    </div>

    ${!isBalanced ? `<div class="warning">⚠ Out of balance by ${formatCurrency(Math.abs(totalDebit - totalCredit))}</div>` : ''}

    <table>
      <thead>
        <tr>
          <th>Account</th>
          <th class="right">Debit</th>
          <th class="right">Credit</th>
        </tr>
      </thead>
      <tbody>
        ${tableRows}
        <tr class="total">
          <td>TOTAL</td>
          <td class="right">${formatCurrency(totalDebit)}</td>
          <td class="right">${formatCurrency(totalCredit)}</td>
        </tr>
      </tbody>
    </table>
    ${isBalanced ? '<div class="warning" style="background:#f0faf0">✓ Balanced</div>' : ''}
  `;

  await printAndShare(wrapHtml('Trial Balance', body), 'trial-balance.pdf');
}

