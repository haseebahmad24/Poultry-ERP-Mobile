function accountTableHtml(rows: TrialBalanceRow[], totalLabel: string, total: number): string {
  const dataRows = rows.map((r) => `
    <tr>
      <td>${r.account_code ? `${r.account_code} — ` : ''}${r.account_name ?? ''}</td>
      <td class="right">${formatCurrency(Math.abs(r.balance ?? Math.abs(r.debit - r.credit)))}</td>
    </tr>`).join('');

  return `
    <table>
      <thead><tr><th>Account</th><th class="right">Amount</th></tr></thead>
      <tbody>
        ${dataRows || '<tr><td colspan="2" class="muted">No accounts found</td></tr>'}
        <tr class="total">
          <td>${totalLabel}</td>
          <td class="right">${formatCurrency(total)}</td>
        </tr>
      </tbody>
    </table>`;
}

